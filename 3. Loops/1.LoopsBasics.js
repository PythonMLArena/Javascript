// Write a Program to print the Numbers from 1 to 10
// 1. Initialize the variable (let i=1)
// 2. Check the number is less than or equal to 10
// 3. Print the number (i)
// 4. Increment i by 1
// 5. go back to step 2

// Looping Control is in Multiple way

///1. WHILE LOOPS 

// Difference between if and while , if only checks the condition once
// While loop checks condition again and again till it become false

let i=1;
while (i<=100){
    console.log(i);
    i+=1;
}

